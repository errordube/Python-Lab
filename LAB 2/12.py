#Rectangle and Circle
PI=3.14
le=float(input('Enter Value of L: '))
bre=float(input('Enter Value of B: '))
rad=float(input('Enter Value of R: '))
AR=le*bre
print("Area of Rect is: ",AR)
PR=2*(le+bre)
print("Perimeter of Rect is: ",PR)
AC=PI*rad*rad
print("Area of Circle is: ",AC)
CC=2*PI*rad
print("Circumference of Circle is: ",CC)

